package wikiindex

import "strings"

func filterStopwords(locale string, terms []string) []string {
	locale = normalizeLocale(locale)
	if locale == "" || len(terms) == 0 {
		return terms
	}

	var sw map[string]struct{}
	switch locale {
	case "ru":
		sw = stopwordsRU
	case "en":
		sw = stopwordsEN
	default:
		return terms
	}

	out := make([]string, 0, len(terms))
	for _, t := range terms {
		if _, ok := sw[t]; ok {
			continue
		}
		out = append(out, t)
	}

	if len(out) == 0 {
		return nil
	}

	return out
}

func normalizeLocale(s string) string {
	switch strings.ToLower(strings.TrimSpace(s)) {
	case "ru", "rus", "russian":
		return "ru"
	case "en", "eng", "english":
		return "en"
	default:
		return ""
	}
}

